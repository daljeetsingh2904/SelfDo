package Thread;

public class ThreadLifecycle {

	/**
	 * A thread in java goes through various states in life:
	 * 
	 * -> New       -A thread that has been created but not yet started.
	 * -> Runnable  -A thread that is ready to run and waiting for CPU time.
	 * -> Blocked   -A thread that is blocked waiting for monitor lock.
	 * -> Waiting   -A thread that is waiting indefinitely for another thread to perform particular action
	 * -> Timed Waiting - A thread that is waiting for another thread to perform an action for upto specified waiting time.
	 * -> Terminated -A thread that has exited.
	 * 
	 *  
	 *  
	 */
}
